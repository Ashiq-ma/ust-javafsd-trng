package com.ust;

public class Switch {
	public static void main(Vowels args[]) {
		int expression=2;
		switch(expression) {
		case 1:
			System.out.println("Case1");
		case 2:
			System.out.println("Case2");
		case 3:
			System.out.println("Case3");
			default:
				System.out.println("Default case");
		}
	}

}
